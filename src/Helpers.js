export function getPlugType(country) {
    let countryData = plugLookup.find(p => p.country === country.value);
    if (countryData === null) {
        return "";
    }
    return countryData.plugTypes.replace(' ', '').split(',');
}

export function getPlugTypeClass(plugLetter) {
    return "type-" + plugLetter.toLocaleLowerCase().replace(' ', '');
}

export function getCountryCompatibleSockets(countryOne, countryTwo) {
    let countryOnePlugTypes = getPlugType(countryOne);
    let countryTwoSockets = getPlugType(countryTwo);
    
    let compatiblePlugs = [];
    for (let plugType of countryOnePlugTypes) {
        let plugData = { plug: plugType, compatibleSockets: [] };
        let compatibleSockets = getCompatibleSocketsForPlug(plugType);
        for(let socket of compatibleSockets) {
            if(countryTwoSockets.indexOf(socket) !== -1) {
                plugData.compatibleSockets.push(socket);
            }
        }

        if(plugData.compatibleSockets.length > 0) {
            compatiblePlugs.push(plugData);
        }
    }

    return compatiblePlugs;
}

function getCompatibleSocketsForPlug(plugType) {
    let compatiblePlugs = [];
    switch (plugType) {
        case "A":
            compatiblePlugs = ["B"];
            break;
        case "C":
            compatiblePlugs = ["E", "F", "H", "J", "K", "L", "N", "O"];
            break;
        case "E":
            compatiblePlugs = ["C", "F", "K", "O"];
            break;
        case "F":
            compatiblePlugs = ["C", "E", "K", "O"];
            break;
        default:
            compatiblePlugs = []
    }

    compatiblePlugs.push(plugType);
    return compatiblePlugs;
}